package com.langfundamentals;

public class Autobun {

	public static void main(String[] args) {
//		Autoboxing
		int num  = 100;
		Integer obj = num;
		
		System.out.println(num);
		System.out.println(obj);
//Auto unboxing
		Integer value = 700;
		int primitive  = value;
		
		System.out.println(value);
		System.out.println(primitive);

	}

}
